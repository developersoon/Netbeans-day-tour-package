    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            int i = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this?", "Confirm", JOptionPane.YES_NO_OPTION);
            if (i == 0) {
                int teDel = DBtable.getSelectedRow();
                String val = (DBtable.getModel().getValueAt(teDel, 0).toString());
                String qry = "DELETE FROM guestprofile WHERE list = " + val;
                pst = con.prepareStatement(qry);
                pst.executeUpdate();

                for (int ii = 0; ii < DBtable.getRowCount(); ii++) {
                    String accno = DBtable.getValueAt(ii, 0).toString();
                    String ln = DBtable.getValueAt(ii, 1).toString();
                    String fn = DBtable.getValueAt(ii, 2).toString();
                    String tp = DBtable.getValueAt(ii, 3).toString();
                    String ap = DBtable.getValueAt(ii, 4).toString();
                    String date = DBtable.getValueAt(ii, 5).toString();

                    pst = con.prepareStatement("INSERT INTO guest_bin(AccN,Lastname,Firstname,Tourpackage,Amountpaid,date) VALUES (?,?,?,?,?,?)");
                    pst.setString(1, accno);
                    pst.setString(2, ln);
                    pst.setString(3, fn);
                    pst.setString(4, tp);
                    pst.setString(5, ap);
                    pst.setString(6, date);

                    pst.executeUpdate();
                }

                DefaultTableModel mods = (DefaultTableModel) DBtable.getModel();
                mods.setRowCount(0);
                Display_Employee();
                JOptionPane.showMessageDialog(this, "Record has been deleted!");
            } else {
                JOptionPane.showMessageDialog(this, "Delete Cancelled!");
            }

        } catch (Exception ex) {
            Logger.getLogger(DayTourPackage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

