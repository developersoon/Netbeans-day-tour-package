    private ArrayList<GuestData> EmpRecord() {
        ArrayList<GuestData> toRecord = new ArrayList<>();
        con = ConnectDB();
        String qwrt = "SELECT * FROM guestprofile";
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(qwrt);
            GuestData rec;
            while (rs.next()) {
                rec = new GuestData(rs.getInt("list"),
                        rs.getString("AccNo"),
                        rs.getString("Lastname"),
                        rs.getString("Firstname"),
                        rs.getString("Tour_package"),
                        rs.getString("Amount_paid"),
                        rs.getString("Date")
                );
                toRecord.add(rec);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        return toRecord;
    }

    private void Display_Employee() {
        ArrayList<GuestData> listOfEmp = EmpRecord();
        DefaultTableModel mo = (DefaultTableModel) DBtable.getModel();
        Object[] row = new Object[7];
        for (int i = 0; i < listOfEmp.size(); i++) {
            row[0] = listOfEmp.get(i).getId();
            row[1] = listOfEmp.get(i).getAccN();
            row[2] = listOfEmp.get(i).getLastname();
            row[3] = listOfEmp.get(i).getFirstname();
            row[4] = listOfEmp.get(i).getTp();
            row[5] = listOfEmp.get(i).getAp();
            row[6] = listOfEmp.get(i).getDate();

            mo.addRow(row);

        }

    }

